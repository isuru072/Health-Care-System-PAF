package com.bean;

public class AdminBean {
	private int Admin_ID;
	private String Admin_Name;
	private String Admin_Gender;
	private String Admin_Address;
	private String Admin_Password;
	private int Admin_Phone;
	private String Admin_Email;
	
	public int getAdmin_ID() {
		return Admin_ID;
	}
	public void setAdmin_ID(int admin_ID) {
		Admin_ID = admin_ID;
	}
	public String getAdmin_Name() {
		return Admin_Name;
	}
	public void setAdmin_Name(String admin_Name) {
		Admin_Name = admin_Name;
	}
	public String getAdmin_Gender() {
		return Admin_Gender;
	}
	public void setAdmin_Gender(String admin_Gender) {
		Admin_Gender = admin_Gender;
	}
	public String getAdmin_Address() {
		return Admin_Address;
	}
	public void setAdmin_Address(String admin_Address) {
		Admin_Address = admin_Address;
	}
	public String getAdmin_Password() {
		return Admin_Password;
	}
	public void setAdmin_Password(String admin_Password) {
		Admin_Password = admin_Password;
	}
	public int getAdmin_Phone() {
		return Admin_Phone;
	}
	public void setAdmin_Phone(int admin_Phone) {
		Admin_Phone = admin_Phone;
	}
	public String getAdmin_Email() {
		return Admin_Email;
	}
	public void setAdmin_Email(String admin_Email) {
		Admin_Email = admin_Email;
	}
	
	
}
